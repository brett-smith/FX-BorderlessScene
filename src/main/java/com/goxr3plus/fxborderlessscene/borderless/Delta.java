package com.goxr3plus.fxborderlessscene.borderless;

/**
 * This class represents a point with x and y values.
 *
 * @author Nicolas Senet-Larson
 * @author GOXR3PLUS STUDIO
 */
public class Delta {
	
	/** The x. */
	public Double x;
	
	/** The y. */
	public Double y;

	@Override
	public String toString() {
		return "Delta [x=" + x + ", y=" + y + "]";
	}
}
